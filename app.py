# ── Load Model ────────────────────────────────────────────────────────────────
@st.cache_resource
def load_artifacts():
    data = joblib.load("final_model.pkl")
    # Support both: plain model OR dict {"model": ..., "features": [...]}
    if isinstance(data, dict):
        return data["model"], data["features"]
    else:
        # Fallback: model saved directly — read feature order from model itself
        return data, list(data.feature_names_in_)

try:
    model, features = load_artifacts()
except FileNotFoundError:
    st.error("⚠️  `final_model.pkl` not found. Run your training script first.")
    st.stop()


# ── Chart helpers ─────────────────────────────────────────────────────────────
BG = "#060d1f"

def gauge_chart(pass_prob):
    fig, ax = plt.subplots(figsize=(4, 2.4), subplot_kw=dict(aspect="equal"))
    fig.patch.set_facecolor(BG)
    ax.set_facecolor(BG)

    theta = np.linspace(np.pi, 0, 300)
    ax.plot(np.cos(theta), np.sin(theta), lw=20, color="#1a2744", solid_capstyle="butt")

    filled = np.linspace(np.pi, np.pi - np.pi * pass_prob / 100, 300)
    color  = "#2ecc71" if pass_prob >= 50 else "#e74c3c"
    ax.plot(np.cos(filled), np.sin(filled), lw=20, color=color, solid_capstyle="butt", alpha=0.9)
    ax.plot(np.cos(filled), np.sin(filled), lw=7,  color=color, solid_capstyle="butt", alpha=0.25)

    angle = np.pi - np.pi * pass_prob / 100
    ax.annotate("", xy=(0.58 * np.cos(angle), 0.58 * np.sin(angle)),
                xytext=(0, 0),
                arrowprops=dict(arrowstyle="-|>", color="white", lw=2.5, mutation_scale=14))
    ax.plot(0, 0, "o", color="white", ms=7, zorder=5)

    ax.text(0, -0.22, f"{pass_prob:.1f}%", ha="center", va="center",
            fontsize=20, fontweight="bold", color="white")
    ax.text(0, -0.50, "Pass Probability", ha="center", va="center",
            fontsize=8.5, color="#718096")

    ax.set_xlim(-1.2, 1.2)
    ax.set_ylim(-0.65, 1.15)
    ax.axis("off")
    plt.tight_layout(pad=0)
    return fig


def bar_chart(pass_prob, fail_prob):
    fig, ax = plt.subplots(figsize=(4, 1.7))
    fig.patch.set_facecolor(BG)
    ax.set_facecolor(BG)

    bars = ax.barh(["  Fail", "  Pass"], [fail_prob, pass_prob],
                   color=["#e74c3c", "#2ecc71"], edgecolor="none", height=0.45)
    for bar, v in zip(bars, [fail_prob, pass_prob]):
        ax.text(v + 1.5, bar.get_y() + bar.get_height() / 2,
                f"{v:.1f}%", va="center", color="white", fontsize=10, fontweight="600")

    ax.set_xlim(0, 120)
    ax.tick_params(axis="y", colors="#cbd5e0", labelsize=10)
    ax.tick_params(axis="x", colors="#4a5568", labelsize=8)
    ax.set_xlabel("Probability (%)", color="#4a5568", fontsize=8)
    for spine in ax.spines.values():
        spine.set_visible(False)
    ax.xaxis.grid(True, color="#1a2744", linewidth=0.8)
    ax.set_axisbelow(True)
    plt.tight_layout(pad=0.6)
    return fig


def profile_chart(user_data):
    RANGES = {
        "weekly_self_study_hours": (0, 40),
        "attendance_percentage":   (0, 100),
        "class_participation":     (0, 10),
    }
    LABELS = {
        "weekly_self_study_hours": "Study Hrs / Week",
        "attendance_percentage":   "Attendance %",
        "class_participation":     "Class Participation",
    }
    COLORS = ["#63b3ed", "#68d391", "#f6ad55"]

    keys   = list(user_data.keys())
    values = [user_data[k] for k in keys]
    norm   = [(v - RANGES[k][0]) / (RANGES[k][1] - RANGES[k][0]) for k, v in zip(keys, values)]
    labels = [LABELS[k] for k in keys]

    fig, ax = plt.subplots(figsize=(6, 2.6))
    fig.patch.set_facecolor(BG)
    ax.set_facecolor(BG)

    ax.barh(labels, [1]*len(labels), color="#1a2744", edgecolor="none", height=0.45)
    bars = ax.barh(labels, norm, color=COLORS, edgecolor="none", height=0.45, alpha=0.9)

    for i, (raw, n) in enumerate(zip(values, norm)):
        ax.text(min(n + 0.03, 1.03), i, f"{raw}", va="center",
                color="white", fontsize=10, fontweight="600")

    ax.set_xlim(0, 1.25)
    ax.tick_params(axis="y", colors="#cbd5e0", labelsize=10)
    ax.tick_params(axis="x", colors="#4a5568", labelsize=8)
    ax.set_xlabel("Relative Score  (0 = min  →  1 = max)", color="#4a5568", fontsize=8)
    for spine in ax.spines.values():
        spine.set_visible(False)
    ax.xaxis.grid(True, color="#1a2744", linewidth=0.8)
    ax.set_axisbelow(True)
    ax.set_title("Student Input Profile", color="#a0aec0", fontsize=10, pad=10, loc="left")
    plt.tight_layout(pad=0.6)
    return fig


